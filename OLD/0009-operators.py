# Arithmetic Operators (+  -  *  /  %  **  //)
# Assignment Operators (=  +=  -=  *=  /=)
# Unary Operators
# Relational Operators (>  <  <=  >=  ==  !=)
# Logical Operators (and  or  not)

# Assignment in one line
a,b = 5,6
print(a,b)

# Unary
n = 7
print(n)
n = -n
print(n)

# Logical
x = False
print(x)
x = not x
print(x)

# Relational
print("===== Relational =====")
print('10' == 10)
print(0 == '')
print(None == False)

print(0 == False)
print(10 == 10)
print(1 == True)
print(bool(None) == False)